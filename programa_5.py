numero = int(input('Digite um numero: '))

if numero == 1:
    print('%d - Domingo' % numero)
elif numero == 2:
    print('%d - Segunda-feira' % numero)
elif numero == 3:
    print('%d - Terça-feira' % numero)
elif numero == 4:
    print('%d - Quarta-feira' % numero)
elif numero == 5:
    print('%d - Quinta-feira' % numero)
elif numero == 6:
    print('%d - Sexta-feira' % numero)
elif numero == 7:
    print('%d - Sabádo' % numero)
else:
    print('%d Não existe!' % numero)